<?php

namespace Rando\BackBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * TypeEvent
 *
 * @ORM\Table(name="type_event")
 * @ORM\Entity(repositoryClass="Rando\BackBundle\Repository\TypeEventRepository")
 */
class TypeEvent
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="titre", type="string", length=255)
     */
    private $titre;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set titre
     *
     * @param string $titre
     *
     * @return TypeEvent
     */
    public function setTitre($titre)
    {
        $this->titre = $titre;

        return $this;
    }

    /**
     * Get titre
     *
     * @return string
     */
    public function getTitre()
    {
        return $this->titre;
    }
}

